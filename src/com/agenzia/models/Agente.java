package com.agenzia.models;

public class Agente {
    private String CF;
    private String nome;
    private String cognome;
    private String ruolo;

    public Agente(String CF, String nome, String cognome, String ruolo) {
        this.CF = CF;
        this.nome = nome;
        this.cognome = cognome;
        this.ruolo = ruolo;
    }

}
